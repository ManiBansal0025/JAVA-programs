/*Create objects of all the wrapper classes 
and print then on console, with using constructor.*/
class  Wrapper1
{
	public static void main(String[] args) 
	{
		Integer a = new Integer(300);
		Float  a1 = new Float(36.4);
		Character a2 = new Character('m');
		Byte a3 = new Byte(2);
		Double a4 = new Double(367.75);
		System.out.println(a);
		System.out.println(a1);
		System.out.println(a2);
		System.out.println(a3);
		System.out.println(a4);
	}
}
